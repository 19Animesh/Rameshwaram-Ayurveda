// ── Determine category for a product ──
function getCategory(name, brand) {
  // Check Tynor products first
  if (brand.includes('TYNOR')) return 'pain-relief';
  
  const upperName = name.toUpperCase();
  
  // Check specific overrides (longest match first)
  const sortedKeys = Object.keys(SPECIFIC_CATEGORIES).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (upperName.includes(key.toUpperCase())) {
      return SPECIFIC_CATEGORIES[key];
    }
  }
  
  // Check form patterns
  for (const [pattern, info] of Object.entries(FORM_PATTERNS)) {
    if (upperName.includes(pattern)) {
      return info.category;
    }
  }
  
  return 'immunity'; // default
}

// ── Generate description for a product ──
function getDescription(name, brand) {
  if (brand.includes('TYNOR')) {
    return TYNOR_DESC(name, brand);
  }
  
  const upperName = name.toUpperCase();
  
  // Check form patterns (longest match first)
  const sortedPatterns = Object.entries(FORM_PATTERNS).sort((a, b) => b[0].length - a[0].length);
  for (const [pattern, info] of sortedPatterns) {
    if (upperName.includes(pattern)) {
      return info.descTemplate(name, brand);
    }
  }
  
  return `${name} by ${brand} is an authentic Ayurvedic medicine prepared according to classical formulations. It supports overall health and wellness.`;
}

// ── Get dosage ──
function getDosage(name, brand) {
  if (brand.includes('TYNOR')) return 'Use as directed';
  const upperName = name.toUpperCase();
  const sortedPatterns = Object.entries(FORM_PATTERNS).sort((a, b) => b[0].length - a[0].length);
  for (const [pattern, info] of sortedPatterns) {
    if (upperName.includes(pattern)) return info.dosage;
  }
  return 'As directed by physician';
}

// ── Deduplicate: keep entry with highest price (larger pack) ──
const deduped = new Map();
for (const [name, brand, price] of RAW_DATA) {
  const key = `${name.toUpperCase().trim()}|${brand.toUpperCase().trim()}`;
  if (!deduped.has(key) || deduped.get(key)[2] < price) {
    deduped.set(key, [name.trim(), brand.trim(), price]);
  }
}

// ── Build catalog ──
const catalog = [];
for (const [name, brand, price] of deduped.values()) {
  const category = getCategory(name, brand);
  const originalPrice = Math.round(price * (1.15 + Math.random() * 0.1)); // 15-25% markup
  
  catalog.push({
    name,
    brand,
    category,
    price,
    originalPrice,
    desc: getDescription(name, brand),
    ingredients: [name.split(' ')[0] + ' extract'],
    dosage: getDosage(name, brand),
    featured: price > 500 || Math.random() < 0.08,
  });
}

// Sort by category then name
catalog.sort((a, b) => a.category.localeCompare(b.category) || a.name.localeCompare(b.name));

